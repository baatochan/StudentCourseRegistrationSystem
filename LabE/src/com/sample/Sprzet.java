package com.sample;


public class Sprzet {

  private long idSprzet;
  private String typ;
  private String marka;
  private String parametry;
  private String stanSprzetu;
  private long idLokalizacji;
  private long idOsoby;
  private String model;


  public long getIdSprzet() {
    return idSprzet;
  }

  public void setIdSprzet(long idSprzet) {
    this.idSprzet = idSprzet;
  }


  public String getTyp() {
    return typ;
  }

  public void setTyp(String typ) {
    this.typ = typ;
  }


  public String getMarka() {
    return marka;
  }

  public void setMarka(String marka) {
    this.marka = marka;
  }


  public String getParametry() {
    return parametry;
  }

  public void setParametry(String parametry) {
    this.parametry = parametry;
  }


  public String getStanSprzetu() {
    return stanSprzetu;
  }

  public void setStanSprzetu(String stanSprzetu) {
    this.stanSprzetu = stanSprzetu;
  }


  public long getIdLokalizacji() {
    return idLokalizacji;
  }

  public void setIdLokalizacji(long idLokalizacji) {
    this.idLokalizacji = idLokalizacji;
  }


  public long getIdOsoby() {
    return idOsoby;
  }

  public void setIdOsoby(long idOsoby) {
    this.idOsoby = idOsoby;
  }


  public String getModel() {
    return model;
  }

  public void setModel(String model) {
    this.model = model;
  }

}
